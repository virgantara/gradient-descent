    <div class="section-block">
      <h2>Research Profile</h2>
      <p>
        Oddy Virgantara Putra is an Associate Professor and researcher in artificial intelligence,
        specializing in low-resource self-supervised learning for 3D point cloud understanding 
        and human-centered intelligent systems. His work focuses on developing adaptive learning 
        mechanisms that integrate self-supervised and active learning strategies to improve 
        data efficiency and robustness in real-world perception systems.
      </p>
      <p>
        His research contributes to the advancement of intelligent systems aligned with cybernetics principles, 
particularly in bridging perception, adaptation, and decision-making. He has been selected as a member 
of the IEEE Systems, Man, and Cybernetics (SMC) Technical Committee on Intelligent Systems to Human-Aware
Sustainability. His work aims to advance scalable and adaptive learning solutions for intelligent 
systems operating under limited supervision.
      </p>
    </div>

